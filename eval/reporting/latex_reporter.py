"""
LaTeX reporter: generates publication-ready tables from evaluation results.
"""

from __future__ import annotations

import os
from typing import Any, Dict, List


class LatexReporter:

    @staticmethod
    def save(
        output_dir: str,
        task: str,
        config: Dict[str, Any],
        aggregate_metrics: Dict[str, float],
        per_sample_results: List[Dict[str, Any]],
        filename: str = "eval_table.tex",
    ) -> str:
        os.makedirs(output_dir, exist_ok=True)
        filepath = os.path.join(output_dir, filename)

        model_name = config.get("model", {}).get("llm_path", "ShapeLLM-Omni")
        model_short = model_name.split("/")[-1]

        lines = []
        lines.append("% Auto-generated by ShapeLLM-Omni Evaluation Framework")
        lines.append(f"% Task: {task}")
        lines.append("")

        metric_names = sorted(aggregate_metrics.keys())
        num_cols = len(metric_names)

        col_spec = "l" + "c" * num_cols
        lines.append(r"\begin{table}[htbp]")
        lines.append(r"  \centering")
        lines.append(f"  \\caption{{Evaluation Results â€” {_escape_latex(task)}}}")
        lines.append(f"  \\label{{tab:eval_{task}}}")
        lines.append(f"  \\begin{{tabular}}{{{col_spec}}}")
        lines.append(r"    \toprule")

        header = "    Model"
        for name in metric_names:
            header += f" & {_format_metric_name(name)}"
        header += r" \\"
        lines.append(header)
        lines.append(r"    \midrule")

        row = f"    {_escape_latex(model_short)}"
        for name in metric_names:
            val = aggregate_metrics[name]
            row += f" & {_format_value(name, val)}"
        row += r" \\"
        lines.append(row)

        lines.append(r"    \bottomrule")
        lines.append(r"  \end{tabular}")
        lines.append(r"\end{table}")
        lines.append("")

        with open(filepath, "w", encoding="utf-8") as f:
            f.write("\n".join(lines))

        print(f"[Reporter] LaTeX table saved to: {filepath}")
        return filepath


def _escape_latex(text: str) -> str:
    for char in ["_", "&", "%", "$", "#", "{", "}"]:
        text = text.replace(char, f"\\{char}")
    return text


def _format_metric_name(name: str) -> str:
    replacements = {
        "bleu_1": "BLEU-1",
        "bleu_2": "BLEU-2",
        "bleu_3": "BLEU-3",
        "bleu_4": "BLEU-4",
        "rouge_l": "ROUGE-L",
        "meteor": "METEOR",
        "cider": "CIDEr",
        "bert_score_f1": "BERTScore",
        "gpt_score": "GPT-Score",
        "voxel_iou": "Voxel IoU",
        "voxel_f1": "Voxel F1",
        "voxel_precision": "Precision",
        "voxel_recall": "Recall",
        "chamfer_distance": "CD $\\downarrow$",
        "emd": "EMD $\\downarrow$",
        "psnr": "PSNR $\\uparrow$",
        "ssim": "SSIM $\\uparrow$",
        "lpips": "LPIPS $\\downarrow$",
        "fid": "FID $\\downarrow$",
        "accuracy": "Acc.",
    }
    return replacements.get(name, _escape_latex(name))


def _format_value(metric_name: str, value: float) -> str:
    if value < 0:
        return "N/A"
    if "iou" in metric_name or "f1" in metric_name or "accuracy" in metric_name:
        return f"{value * 100:.1f}\\%"
    if metric_name in ("psnr",):
        return f"{value:.2f}"
    if metric_name in ("chamfer_distance", "emd"):
        return f"{value:.4f}"
    return f"{value:.3f}"
